    def _new_xo(self, p1, p2, vs_bot):
        return {
            "type":    "xo",
            "board":   [" "] * 9,
            "players": [p1, p2],
            "turn":    0,        # 0=p1(❌)  1=p2(⭕)
            "vs_bot":  vs_bot,
            "symbols": ["❌", "⭕"],
        }

    def _xo_board_str(self, game):
        b = game["board"]
        def cell(i):
            return b[i] if b[i] != " " else str(i + 1)
        return (
            f" {cell(0)} | {cell(1)} | {cell(2)} \n"
            f"───────────\n"
            f" {cell(3)} | {cell(4)} | {cell(5)} \n"
            f"───────────\n"
            f" {cell(6)} | {cell(7)} | {cell(8)} "
        )

    def _xo_check_winner(self, board):
        wins = [(0,1,2),(3,4,5),(6,7,8),(0,3,6),(1,4,7),(2,5,8),(0,4,8),(2,4,6)]
        for a,b,c in wins:
            if board[a] == board[b] == board[c] != " ":
                return board[a]
        return None

    def _xo_bot_move(self, board):
        # أولوية: فوز البوت ← منع الفوز ← المركز ← الزوايا ← عشوائي
        for sym, target in [("⭕", "win"), ("❌", "block")]:
            for i in range(9):
                if board[i] == " ":
                    board[i] = sym
                    if self._xo_check_winner(board):
                        board[i] = " "
                        return i
                    board[i] = " "
        if board[4] == " ": return 4
        for i in [0,2,6,8]:
            if board[i] == " ": return i
        return random.choice([i for i in range(9) if board[i] == " "])

    async def _handle_xo(self, room, nick, msg, game):
        async def reply(text):
            await self.conn.send(
                f"<message to='{room}' type='groupchat'>"
                f"<body>{text}</body></message>"
            )
        try:
            move = int(msg) - 1
            if move < 0 or move > 8:
                return
        except ValueError:
            return

        current_player = game["players"][game["turn"]]
        if nick != current_player:
            return
        if game["board"][move] != " ":
            await reply("⚠️ الخانة محجوزة، اختار غيرها.")
            return

        sym = game["symbols"][game["turn"]]
        game["board"][move] = sym
        winner_sym = self._xo_check_winner(game["board"])

        if winner_sym:
            winner = game["players"][game["symbols"].index(winner_sym)]
            await reply(self._xo_board_str(game) + f"\n\n🎉 {winner} فاز!")
            self.add_points(room, winner, 40)
            del self.games[room]
            return

        if " " not in game["board"]:
            await reply(self._xo_board_str(game) + "\n\n🤝 تعادل!")
            del self.games[room]
            return

        game["turn"] = 1 - game["turn"]

        # دور البوت
        if game["vs_bot"] and game["turn"] == 1:
            bot_move = self._xo_bot_move(game["board"])
            game["board"][bot_move] = "⭕"
            winner_sym = self._xo_check_winner(game["board"])
            if winner_sym:
                await reply(self._xo_board_str(game) + "\n\n🤖 البوت فاز!")
                del self.games[room]
                return
            if " " not in game["board"]:
                await reply(self._xo_board_str(game) + "\n\n🤝 تعادل!")
                del self.games[room]
                return
            game["turn"] = 0
            next_player = game["players"][0]
            await reply(self._xo_board_str(game) + f"\n\nدورك {next_player} ❌")
        else:
            next_player = game["players"][game["turn"]]
            next_sym    = game["symbols"][game["turn"]]
            await reply(self._xo_board_str(game) + f"\n\nدور {next_player} {next_sym}")

