# فئة الاتصال XMPP مع تحليل آمن لـ XML
# ============================================================
class XMPPConn:
    def __init__(self, jid, password):
        self.jid = jid
        self.password = password
        self.reader = None
        self.writer = None
        self.buffer = ""

    async def connect(self):
        try:
            self.reader, self.writer = await asyncio.open_connection(SERVER, PORT)
            await self.send(f"<?xml version='1.0'?><stream:stream to='{SERVER}' xmlns='jabber:client' xmlns:stream='http://etherx.jabber.org/streams' version='1.0'>")
            auth = base64.b64encode(f"\0{self.jid.split('@')[0]}\0{self.password}".encode()).decode()
            await self.send(f"<auth xmlns='urn:ietf:params:xml:ns:xmpp-sasl' mechanism='PLAIN'>{auth}</auth>")
            await asyncio.sleep(2)
            await self.send(f"<?xml version='1.0'?><stream:stream to='{SERVER}' xmlns='jabber:client' xmlns:stream='http://etherx.jabber.org/streams' version='1.0'>")
            await self.send("<iq type='set' id='b1'><bind xmlns='urn:ietf:params:xml:ns:xmpp-bind'><resource>GamesV3.5</resource></bind></iq>")
            await self.send("<presence><priority>10</priority></presence>")
            return True
        except Exception as e:
            logging.error(f"فشل الاتصال: {e}")
            return False

    async def send(self, data):
        if self.writer:
            self.writer.write(data.encode())
            await self.writer.drain()

    async def recv(self):
        if self.reader:
            try:
                data = await self.reader.read(4096)
                return data.decode(errors='ignore')
            except:
                return ""
        return ""

    def safe_parse_xml(self, xml_string):
        """تحليل آمن لـ XML باستخدام defusedxml إذا كان متوفراً"""
        try:
            # محاولة استيراد defusedxml للتحليل الآمن
            import defusedxml.ElementTree as SafeET
            return SafeET.fromstring(xml_string)
        except ImportError:
            # إذا لم تكن المكتبة متوفرة، استخدم ElementTree مع تعطيل الكيانات الخارجية (متاح في Python 3.8+)
            parser = ET.XMLParser(resolve_entities=False)
            return ET.fromstring(xml_string, parser=parser)

