class XMPPConn:
    def __init__(self):
        self.reader = None
        self.writer = None
        self.buffer = ""

    async def connect(self):
        try:
            self.reader, self.writer = await asyncio.open_connection(SERVER, PORT)

            await self._send_raw(
                f"<?xml version='1.0'?>"
                f"<stream:stream to='{SERVER}' xmlns='jabber:client' "
                f"xmlns:stream='http://etherx.jabber.org/streams' version='1.0'>"
            )
            await asyncio.sleep(1)

            auth_str = f"\0{JID.split('@')[0]}\0{PASSWORD}"
            auth_b64 = base64.b64encode(auth_str.encode()).decode()
            await self._send_raw(
                f"<auth xmlns='urn:ietf:params:xml:ns:xmpp-sasl' "
                f"mechanism='PLAIN'>{auth_b64}</auth>"
            )
            await asyncio.sleep(1)

            await self._send_raw(
                f"<?xml version='1.0'?>"
                f"<stream:stream to='{SERVER}' xmlns='jabber:client' "
                f"xmlns:stream='http://etherx.jabber.org/streams' version='1.0'>"
            )
            await asyncio.sleep(1)

            await self._send_raw(
                f"<iq type='set' id='bind1'>"
                f"<bind xmlns='urn:ietf:params:xml:ns:xmpp-bind'>"
                f"<resource>{RESOURCE}</resource></bind></iq>"
            )
            await asyncio.sleep(0.5)

            await self._send_raw(
                "<iq type='set' id='sess1'>"
                "<session xmlns='urn:ietf:params:xml:ns:xmpp-session'/></iq>"
            )
            await asyncio.sleep(0.5)

            await self._send_raw(
                "<presence><priority>10</priority><show>chat</show></presence>"
            )

            logging.info("✅ تم تسجيل الدخول بنجاح!")
            return True
        except Exception as e:
            logging.error(f"❌ فشل الاتصال: {e}")
            return False

    async def _send_raw(self, data):
        self.writer.write(data.encode())
        await self.writer.drain()

    async def send(self, data):
        await self._send_raw(data)

    async def recv(self):
        if self.reader:
            try:
                data = await self.reader.read(4096)
                return data.decode(errors="ignore")
            except:
                return ""
        return ""

    def safe_parse_xml(self, xml_string):
        try:
            import defusedxml.ElementTree as SafeET
            return SafeET.fromstring(xml_string)
        except ImportError:
            parser = ET.XMLParser(resolve_entities=False)
            return ET.fromstring(xml_string, parser=parser)


