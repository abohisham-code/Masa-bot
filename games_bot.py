class GamesBot:
    def __init__(self, conn):
        self.conn      = conn
        self.games     = {}
        self.mem       = self.load_memory()
        self.active_rooms = set(ROOMS)

    # ──── ذاكرة ────────────────────────────────────────────
    def load_memory(self):
        if os.path.exists(MEMORY_FILE):
            try:
                with open(MEMORY_FILE, "r", encoding="utf-8") as f:
                    return json.load(f)
            except:
                pass
        return {"points": {}}

    def save_memory(self):
        try:
            with open(MEMORY_FILE, "w", encoding="utf-8") as f:
                json.dump(self.mem, f, ensure_ascii=False)
        except Exception as e:
            logging.error(f"خطأ في حفظ الذاكرة: {e}")

    # ──── نقاط ─────────────────────────────────────────────
    def add_points(self, room, nick, points):
        if room not in self.mem["points"]:
            self.mem["points"][room] = {}
        if nick not in self.mem["points"][room]:
            self.mem["points"][room][nick] = 0

        old_pts = self.mem["points"][room][nick]
        new_pts = old_pts + points
        self.mem["points"][room][nick] = new_pts
        self.save_memory()

        for threshold in sorted(RANKS.keys(), reverse=True):
            if new_pts >= threshold > old_pts:
                asyncio.create_task(self.conn.send(
                    f"<message to='{room}' type='groupchat'>"
                    f"<body>🎉 كفو {nick}! صرت: {RANKS[threshold]}</body></message>"
                ))
                break

    # ──── الانضمام والمغادرة ───────────────────────────────
    async def join_rooms(self):
        for room in self.active_rooms:
            await self._join_single_room(room)

    async def _join_single_room(self, room):
        nick = ROOM_NICKS.get(room, DEFAULT_NICK)
        logging.info(f"🚪 الانضمام إلى {room} باسم {nick}")
        await self.conn.send(
            f"<presence to='{room}/{nick}'>"
            f"<x xmlns='http://jabber.org/protocol/muc'/></presence>"
        )
        await asyncio.sleep(0.5)
        # إرسال بانر التشغيل للغرفة
        await self.conn.send(
            f"<message to='{room}' type='groupchat'>"
            f"<body>{BANNER}</body></message>"
        )

    async def _leave_single_room(self, room):
        nick = ROOM_NICKS.get(room, DEFAULT_NICK)
        logging.info(f"👋 مغادرة {room}")
        await self.conn.send(
            f"<presence to='{room}/{nick}' type='unavailable'/>"
        )

    # ──── تغيير لقب البوت في غرفة ─────────────────────────
    async def _change_nick_in_room(self, room, new_nick):
        """مغادرة الغرفة بالاسم القديم ثم الدخول بالاسم الجديد."""
        old_nick = ROOM_NICKS.get(room, DEFAULT_NICK)
        # مغادرة
        await self.conn.send(
            f"<presence to='{room}/{old_nick}' type='unavailable'/>"
        )
        await asyncio.sleep(0.5)
        # تحديث الاسم
        ROOM_NICKS[room] = new_nick
        # دخول مجدداً
        await self.conn.send(
            f"<presence to='{room}/{new_nick}'>"
            f"<x xmlns='http://jabber.org/protocol/muc'/></presence>"
        )

    # ──── حلقة الاستقبال ───────────────────────────────────
    async def run(self):
        await self.join_rooms()

        while True:
            data = await self.conn.recv()
            if not data:
                await asyncio.sleep(1)
                continue

            self.conn.buffer += data

            # معالجة stanzas الحضور (presence) للترحيب
            while "</presence>" in self.conn.buffer:
                idx = self.conn.buffer.find("</presence>") + 11
                stanza = self.conn.buffer[:idx]
                self.conn.buffer = self.conn.buffer[idx:]
                try:
                    root = self.conn.safe_parse_xml(stanza)
                    from_attr = root.attrib.get("from", "")
                    ptype     = root.attrib.get("type", "available")
                    if "/" in from_attr and ptype != "unavailable":
                        room, joiner = from_attr.split("/", 1)
                        bot_nicks = list(ROOM_NICKS.values()) + [DEFAULT_NICK]
                        if joiner not in bot_nicks and room in self.active_rooms:
                            await self._handle_join(room, joiner)
                except:
                    pass

            # معالجة الرسائل
            while "</message>" in self.conn.buffer:
                idx = self.conn.buffer.find("</message>") + 10
                stanza = self.conn.buffer[:idx]
                self.conn.buffer = self.conn.buffer[idx:]
                try:
                    root = self.conn.safe_parse_xml(stanza)
                    body = (
                        root.find(".//{jabber:client}body")
                        or root.find("body")
                    )
                    if body is None or body.text is None:
                        continue

                    from_attr = root.attrib.get("from", "")
                    if "/" in from_attr:
                        room, sender = from_attr.split("/", 1)
                    else:
                        room, sender = from_attr, ""

                    bot_nicks = list(ROOM_NICKS.values()) + [DEFAULT_NICK]
                    if sender in bot_nicks:
                        continue

                    await self.handle_message(room, sender, body.text.strip())
                except Exception as e:
                    logging.error(f"خطأ في معالجة الرسالة: {e}")

