# فئة البوت الرئيسية
# ============================================================
class GamesBot:
    def __init__(self, conn):
        self.conn = conn
        self.games = {}
        self.mem = self.load_memory()

    def load_memory(self):
        """تحميل نقاط اللاعبين من الملف"""
        if os.path.exists(MEMORY_FILE):
            try:
                with open(MEMORY_FILE, 'r', encoding='utf-8') as f:
                    return json.load(f)
            except:
                pass
        return {"points": {}}

    def save_memory(self):
        """حفظ النقاط إلى الملف"""
        try:
            with open(MEMORY_FILE, 'w', encoding='utf-8') as f:
                json.dump(self.mem, f, ensure_ascii=False)
        except Exception as e:
            logging.error(f"خطأ في حفظ الذاكرة: {e}")

    def add_points(self, room, nick, points):
        """إضافة نقاط للاعب والتحقق من الترقية"""
        if room not in self.mem["points"]:
            self.mem["points"][room] = {}
        if nick not in self.mem["points"][room]:
            self.mem["points"][room][nick] = 0

        old_points = self.mem["points"][room][nick]
        new_points = old_points + points
        self.mem["points"][room][nick] = new_points
        self.save_memory()

        # فحص الترقية إلى رتبة جديدة
        for threshold in sorted(RANKS.keys(), reverse=True):
            if new_points >= threshold > old_points:
                rank_name = RANKS[threshold]
                asyncio.create_task(self.conn.send(
                    f"<message to='{room}' type='groupchat'><body>🎉 كفو {nick}! صرت: {rank_name}</body></message>"
                ))
                break

    async def join_rooms(self):
        """الانضمام إلى جميع الغرف المحددة"""
        for room in ROOMS:
            nick = ROOM_NICKS.get(room, DEFAULT_NICK)
            await self.conn.send(f"<presence to='{room}/{nick}'><x xmlns='http://jabber.org/protocol/muc'/></presence>")
            await asyncio.sleep(0.5)

    async def run(self):
        """الحلقة الرئيسية لاستقبال ومعالجة الرسائل"""
        await self.join_rooms()

        while True:
            data = await self.conn.recv()
            if not data:
                await asyncio.sleep(1)
                continue

            self.conn.buffer += data
            while "</message>" in self.conn.buffer:
                idx = self.conn.buffer.find("</message>") + 10
                stanza = self.conn.buffer[:idx]
                self.conn.buffer = self.conn.buffer[idx:]

                try:
                    root = self.conn.safe_parse_xml(stanza)
                    body = root.find(".//{jabber:client}body") or root.find("body")
                    if body is None or body.text is None:
                        continue

                    from_attr = root.attrib.get("from", "")
                    if "/" in from_attr:
                        room, sender = from_attr.split("/", 1)
                    else:
                        room, sender = from_attr, ""

                    # تجاهل رسائل البوت نفسه
                    if sender in ROOM_NICKS.values():
                        continue

                    await self.handle_message(room, sender, body.text.strip())
                except Exception as e:
                    logging.error(f"خطأ في معالجة الرسالة: {e}")

    async def handle_message(self, room, nick, msg):
        """معالجة الأمر أو الرسالة الواردة"""
        # دالة مساعدة للرد السريع
        async def reply(text):
            await self.conn.send(f"<message to='{room}' type='groupchat'><body>{text}</body></message>")

        # فحص الألعاب النشطة
        game = self.games.get(room)

        # لعبة تخمين الرقم
        if game and game["type"] == "guess":
            try:
                guess = int(msg)
                if guess == game["answer"]:
                    await reply(f"🎯 صح {nick}! الرقم {guess}")
                    self.add_points(room, nick, 30)
                    del self.games[room]
                elif guess < game["answer"]:
                    await reply("📈 أكبر")
                else:
                    await reply("📉 أصغر")
            except ValueError:
                pass
            return

        # لعبة الأسئلة
        if game and game["type"] == "trivia":
            if msg.lower() == game["answer"].lower():
                await reply(f"✅ كفو {nick}! الجواب: {game['answer']}")
                self.add_points(room, nick, 30)
                del self.games[room]
            return

        # معالجة الأوامر النصية
        if msg == "اوامر":
            await reply(
                "🎮 الألعاب:\n"
                "- تخمين رقم | نرد | سلوت\n"
                "- سؤال (ثقافي، رياضي، ديني، نسواني، رجالي، فني)\n"
                "- فزورة | صراحة\n"
                "- نقاطي | توب"
            )
        elif msg == "تخمين رقم":
            self.games[room] = {"type": "guess", "answer": random.randint(1, 100)}
            await reply("🎯 خمنت رقم (1-100)")
        elif msg.startswith("سؤال ") or msg in ["فزورة", "سؤال فني"]:
            if msg.startswith("سؤال "):
                cat = msg.replace("سؤال ", "")
            elif msg == "فزورة":
                cat = "فزورة"
            else:
                cat = "فني"

            if cat in QUESTIONS_DATA:
                q = random.choice(QUESTIONS_DATA[cat])
                self.games[room] = {"type": "trivia", "answer": q["a"]}
                await reply(f"{q['q']} ⏳")
            else:
                await reply("❌ الفئة غير موجودة")
        elif msg == "نرد":
            dice = random.randint(1, 6)
            await reply(f"🎲 {nick} طلع له: {dice}")
        elif msg == "سلوت":
            symbols = [random.choice(["💎", "⭐", "🔔"]) for _ in range(3)]
            result = " | ".join(symbols)
            await reply(f"🎰 [ {result} ]")
            if symbols[0] == symbols[1] == symbols[2]:
                self.add_points(room, nick, 50)
                await reply("🎉 جاكبوت!")
        elif msg == "نقاطي":
            points = self.mem["points"].get(room, {}).get(nick, 0)
            rank = next((RANKS[t] for t in sorted(RANKS.keys(), reverse=True) if points >= t), RANKS[0])
            await reply(f"👤 {nick}\n⭐ نقاطك: {points}\n🏅 لقبك: {rank}")
        elif msg == "توب":
            room_points = self.mem["points"].get(room, {})
            top_players = sorted(room_points.items(), key=lambda x: x[1], reverse=True)[:5]
            if top_players:
                lines = [f"{i+1}. {p[0]} ({p[1]})" for i, p in enumerate(top_players)]
                await reply("🏆 الأساطير:\n" + "\n".join(lines))
            else:
                await reply("🏆 لا يوجد لاعبين بعد!")

