class GamesBot:
    def __init__(self, conn, nick):
        self.conn   = conn
        self.nick   = nick
        self.rooms  = list(ROOMS)
        self.memory = self.load_memory()
        self.memory.setdefault("points", {})
        self.memory.setdefault("admins", [])
