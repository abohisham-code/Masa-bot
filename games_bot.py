# دالة تطبيع النص للمقارنة المرنة
# ============================================================
def normalize(text):
    """يزيل الهمزات والتشكيل والفروقات الشائعة قبل المقارنة"""
    import re
    t = text.strip().lower()
    # توحيد الأرقام العربية → إنجليزية
    arabic_nums = '٠١٢٣٤٥٦٧٨٩'
    for i, c in enumerate(arabic_nums):
        t = t.replace(c, str(i))
    # توحيد الأرقام المؤشرة (superscript) → إنجليزية
    superscript = {'⁰':'0','¹':'1','²':'2','³':'3','⁴':'4',
                   '⁵':'5','⁶':'6','⁷':'7','⁸':'8','⁹':'9'}
    for k, v in superscript.items():
        t = t.replace(k, v)
    # توحيد الهمزات والألف
    t = re.sub(r'[أإآا]', 'ا', t)
    t = re.sub(r'[ئءؤ]', 'ء', t)
    # توحيد الهاء والتاء المربوطة
    t = t.replace('ة', 'ه')
    # توحيد الياء
    t = t.replace('ى', 'ي')
    # توحيد الذال/الزاي في حالات شائعة (ذهب/دهب)
    t = t.replace('ذ', 'ز')
    # إزالة التشكيل
    t = re.sub(r'[\u064b-\u065f]', '', t)
    # حذف ال التعريف من البداية
    t = re.sub(r'^ال', '', t)
    # إزالة المسافات الزائدة
    t = re.sub(r'\s+', ' ', t).strip()
    return t


