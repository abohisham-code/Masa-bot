async def start_bot():
    # تشغيل خادم Flask في خيط منفصل
    flask_thread = Thread(target=run_flask, daemon=True)
    flask_thread.start()

    # إعداد التسجيل
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(levelname)s - %(message)s'
    )

    # محاولة الاتصال وإعادة الاتصال عند الفشل
    while True:
        conn = XMPPConn(JID, PASSWORD)
        if await conn.connect():
            logging.info("✅ تم الاتصال بالخادم")
            bot = GamesBot(conn)
            await bot.run()
        else:
            logging.error("❌ فشل الاتصال، إعادة المحاولة بعد 10 ثوان...")
            await asyncio.sleep(10)

if __name__ == "__main__":
    asyncio.run(start_bot())
